/**
 * @file nl_phy_common.c
 * @brief 6.5.1 CRC / 6.5.2 Gold / 6.5.3 QPSK 参考信号实现
 *
 * Gold 序列 x1 预计算说明：
 *   x1 序列递推公式：x1(n+31) = (x1(n+3) + x1(n)) mod 2
 *   初始条件：x1(0)=1, x1(1..30)=0
 *   由于 x1 初始值固定（与 c_init 无关），其前 10001 个比特已预计算
 *   并打包存储于 nl_pn_x1_table[]（8 bit/byte，LSB 优先）。
 *   运行时仅需迭代 x2 LFSR，x1 比特通过查表获取。
 */
#include "nl_phy_common.h"

#include <math.h>
#include <string.h>

/* ====== 6.5.1 CRC ====== */

typedef struct {
    uint32_t poly; /* 低 L 位（不含 D^L 项） */
    uint32_t L;
} nl_crc_desc_t;

static nl_crc_desc_t nl_crc_get_desc(nl_crc_poly_t poly)
{
    switch (poly) {
    case NL_CRC24A:
        return (nl_crc_desc_t){0x864CFBu, 24u};
    case NL_CRC24B:
        return (nl_crc_desc_t){0x800063u, 24u};
    case NL_CRC16:
        return (nl_crc_desc_t){0x1021u, 16u};
    case NL_CRC8:
        return (nl_crc_desc_t){0x9Bu, 8u};
    case NL_CRC6:
        return (nl_crc_desc_t){0x21u, 6u};
    default:
        return (nl_crc_desc_t){0u, 0u};
    }
}

uint32_t nl_crc_get_len(nl_crc_poly_t poly)
{
    nl_crc_desc_t d = nl_crc_get_desc(poly);
    return d.L;
}

static void nl_crc_calc_remainder(const uint8_t *bits, uint32_t n, uint32_t poly, uint32_t L,
                                  uint8_t *parity)
{
    uint32_t reg = 0u;
    uint32_t mask = (L >= 32u) ? 0xFFFFFFFFu : ((1u << L) - 1u);

    for (uint32_t i = 0u; i < n + L; i++) {
        uint32_t bit = (i < n) ? (uint32_t)(bits[i] & 1u) : 0u;
        uint32_t top = (reg >> (L - 1u)) & 1u;
        reg = ((reg << 1u) | bit) & mask;
        if (top ^ bit) {
            reg ^= poly;
        }
    }

    for (uint32_t j = 0u; j < L; j++) {
        parity[j] = (uint8_t)((reg >> (L - 1u - j)) & 1u);
    }
}

int nl_crc_encode(const nl_crc_encode_cfg_t *cfg)
{
    nl_crc_desc_t d;
    uint8_t       parity[24];

    if (cfg == NULL || cfg->a == NULL || cfg->b == NULL) {
        return NL_PHY_ERR_PARAM;
    }

    d = nl_crc_get_desc(cfg->poly);
    if (d.L == 0u) {
        return NL_PHY_ERR_PARAM;
    }
    if (cfg->b_cap < cfg->A + d.L) {
        return NL_PHY_ERR_BUF;
    }

    memcpy(cfg->b, cfg->a, cfg->A);
    nl_crc_calc_remainder(cfg->a, cfg->A, d.poly, d.L, parity);

    for (uint32_t i = 0u; i < d.L; i++) {
        uint8_t p = parity[i];
        if (d.L == 24u && cfg->mask != NULL) {
            p = (uint8_t)((p + (cfg->mask[i] & 1u)) & 1u);
        }
        cfg->b[cfg->A + i] = p;
    }
    return NL_PHY_OK;
}

int nl_crc_check(const nl_crc_check_cfg_t *cfg)
{
    nl_crc_desc_t d;
    uint32_t      reg = 0u;
    uint32_t      mask;

    if (cfg == NULL || cfg->b == NULL || cfg->pass == NULL) {
        return NL_PHY_ERR_PARAM;
    }

    d = nl_crc_get_desc(cfg->poly);
    if (d.L == 0u || cfg->B == 0u) {
        return NL_PHY_ERR_PARAM;
    }

    mask = (d.L >= 32u) ? 0xFFFFFFFFu : ((1u << d.L) - 1u);
    for (uint32_t i = 0u; i < cfg->B; i++) {
        uint32_t bit = (uint32_t)(cfg->b[i] & 1u);
        uint32_t top = (reg >> (d.L - 1u)) & 1u;
        reg          = ((reg << 1u) | bit) & mask;
        if (top ^ bit) {
            reg ^= d.poly;
        }
    }
    *cfg->pass = (reg == 0u) ? 1u : 0u;
    return NL_PHY_OK;
}

