int nl_pn_gold_generate(const nl_pn_seq_cfg_t *cfg)
{
    uint32_t x2;
    uint32_t x2_bit;
    uint32_t n;
    uint32_t i;
    uint32_t x1_idx;

    if (cfg == NULL || cfg->c == NULL || cfg->M_PN == 0u) {
        return NL_PHY_ERR_PARAM;
    }
    if (cfg->c_cap < cfg->M_PN) {
        return NL_PHY_ERR_BUF;
    }
    if (cfg->M_PN > (NL_PN_X1_MAX_IDX - NL_PN_X1_OUT_OFFSET)) {
        return NL_PHY_ERR_BUF;
    }

    /* x2 LFSR 预热 1600 次（x1 已预计算，无需迭代） */
    x2 = cfg->c_init & 0x7FFFFFFFu;
    for (i = 0u; i < NL_PN_X1_WARMUP; i++) {
        x2 = (x2 >> 1u)
             | ((((x2 >> 3u) ^ (x2 >> 2u) ^ (x2 >> 1u) ^ x2) & 1u) << 30u);
    }

    /* 输出：c[n] = x1(1630+n) XOR x2_output_bit */
    x1_idx = NL_PN_X1_OUT_OFFSET;
    for (n = 0u; n < cfg->M_PN; n++) {
        x2_bit   = (x2 >> 30u) & 1u;
        cfg->c[n] = (uint8_t)(NL_PN_X1_GET_BIT(x1_idx) ^ x2_bit);
        x1_idx++;
        x2 = (x2 >> 1u)
             | ((((x2 >> 3u) ^ (x2 >> 2u) ^ (x2 >> 1u) ^ x2) & 1u) << 30u);
    }
    return NL_PHY_OK;
}

/* ====== 6.5.3 伪随机 QPSK 参考信号 ====== */

uint32_t nl_qpsk_calc_cinit(const nl_qpsk_cinit_param_t *param)
{
    uint32_t term1;
    uint32_t term2;

    if (param == NULL) {
        return 0u;
    }

    term1 = (uint32_t)(15u * ((uint32_t)param->n + 1u) + (uint32_t)param->l + 1u);
    term2 = (uint32_t)(4u * (uint32_t)param->N_ID + 1u);
    return (uint32_t)((1u << 10) * term1 * term2 + 4u * (uint32_t)param->N_ID
                      + (uint32_t)param->N_CP);
}

int nl_qpsk_ref_generate(const nl_qpsk_seq_cfg_t *cfg)
{
    uint32_t        c_init;
    uint8_t         gold[322];
    nl_pn_seq_cfg_t pn;
    float           scale;
    uint32_t        m;

    if (cfg == NULL || cfg->r == NULL) {
        return NL_PHY_ERR_PARAM;
    }
    if (cfg->r_cap < NL_QPSK_N_SC) {
        return NL_PHY_ERR_BUF;
    }

    c_init = nl_qpsk_calc_cinit(&cfg->param);
    pn.c_init = c_init;
    pn.M_PN   = 322u;
    pn.c      = gold;
    pn.c_cap  = 322u;
    if (nl_pn_gold_generate(&pn) != NL_PHY_OK) {
        return NL_PHY_ERR_PARAM;
    }

    scale = 1.0f / sqrtf(2.0f);
    for (m = 0u; m < NL_QPSK_N_SC; m++) {
        float i_val = scale * (2.0f * (float)gold[2u * m] - 1.0f);
        float q_val = scale * (2.0f * (float)gold[2u * m + 1u] - 1.0f);
        cfg->r[m].re = i_val;
        cfg->r[m].im = q_val;
    }
    return NL_PHY_OK;
}

int nl_qpsk_map_to_re(const nl_cplx_t *r, nl_cplx_t *a_kl, uint32_t k)
{
    if (r == NULL || a_kl == NULL) {
        return NL_PHY_ERR_PARAM;
    }
    if (k >= NL_QPSK_N_SC) {
        return NL_PHY_ERR_PARAM;
    }
    if (k == NL_QPSK_DC_K) {
        a_kl->re = 0.0f;
        a_kl->im = 0.0f;
    } else {
        *a_kl = r[k];
    }
    return NL_PHY_OK;
}
